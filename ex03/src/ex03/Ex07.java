package ex03;

public class Ex07 {
	public static void main(String[] args) {
		
		// \t 탭만 큼 띄워쓰기
		System.out.println("a\tb\tc");
		
		System.out.println();
		
		// \b 하면 이 부분 앞에 있는 문자?숫자? 지워짐
		System.out.println("ddd\beee\b");
		
		// "안녕하세요"
		System.out.println("\"안녕하세요\"");
		
		// c:\
		System.out.println("c:\\");
	}
}
